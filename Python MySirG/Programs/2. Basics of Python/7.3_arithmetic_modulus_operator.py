# Modulus Operator (%):-
x=4%3
print(x)

x=11%7
print(x)

x=2%3
print(x)

x=4.5%3 # In Python, we can use modulus operator for float values also
print(x)

x=2%3.0
print(x)

# Negative Modulus Operator
